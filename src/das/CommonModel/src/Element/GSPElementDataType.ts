/**
 * 字段数据类型
 */
enum GSPElementDataType {
    /**
     * 字符串
     */
    String,

    /**
     * 长文本
     */
    Text,

    /**
     * 整形
     */
    Integer,

    /**
     * 十进制
     */
    Decimal,

    /**
     * 布尔型
     */
    Boolean,

    /**
     * 日期型
     */
    Date,

    /**
     * 日期时间型
     */
    DateTime,

    /**
     * 二进制
     */
    Binary
}

export { GSPElementDataType }
